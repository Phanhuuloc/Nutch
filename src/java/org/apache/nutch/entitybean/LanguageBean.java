package org.apache.nutch.entitybean;

public class LanguageBean {
	private String code;
	private String language;
	private String languageTranslate;
	private String languageTranslateCode;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getLanguage() {
		return language;
	}

	public void setLanguage(String language) {
		this.language = language;
	}

	public String getLanguageTranslate() {
		return languageTranslate;
	}

	public void setLanguageTranslate(String languageTranslate) {
		this.languageTranslate = languageTranslate;
	}

	public String getLanguageTranslateCode() {
		return languageTranslateCode;
	}

	public void setLanguageTranslateCode(String languageTranslateCode) {
		this.languageTranslateCode = languageTranslateCode;
	}

}
